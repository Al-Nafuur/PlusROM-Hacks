;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;
; This example demonstrates an asymmetric playfield, which
; allows different patterns for the left and right sides of
; the playfield, giving you 40 unique playfied pixels per line.
;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	seg.u Variables
	org $80
XPos		    .byte   ; 0(3) - 168(161) East to west
YPos		    .byte   ; 0-85 North to South
			    .byte   ; YPos hb
FrameCounter    .byte
BigCounter      .byte
RequestPending  .byte

	seg Code
	org $f000

Start
	CLEAN_START

; First request backend data
    sta WriteSendBuffer

; Start pos of ISS
	lda #81
	sta XPos
	lda #49
	sta YPos

    lda #30
    sta FrameCounter
    sta BigCounter

	lda #>ISSPlayerData
	sta YPos+1

; Set colors
	lda #WHITE
	sta COLUP0
	lda #BLUE_CYAN|$8
	sta COLUBK
	lda #GREEN|$2
	sta COLUPF

NextFrame
	VERTICAL_SYNC

	TIMER_SETUP 37

	lda SWCHB	; read Console Reset switch for PlusCart exit function
	lda SWCHA	; read Joystick switch for PlusCart exit function

    dec FrameCounter
    bne _skipIncrementBigCounter
    lda #30
    sta FrameCounter
    dec BigCounter
    bne _skipIncrementBigCounter
    sta BigCounter
    sta WriteSendBuffer

_skipIncrementBigCounter

	lda #2	               ; wait for 2 bytes response
	cmp ReceiveBufferSize  ; in ReceiveBuffer
    bne _skipPlusROMLoad
    lda ReceiveBuffer
    sta XPos
    lda ReceiveBuffer
    sta YPos
_skipPlusROMLoad

	lda XPos	; get X coordinate
	ldx #0		; player 0
	jsr SetHorizPos	; set coarse offset
	sta WSYNC	; sync w/ scanline
	sta HMOVE	; apply fine offsets


	TIMER_WAIT


	ldy #96
	ldx #192
ScanLoop
	clc
	lda (YPos),y
; WSYNC and store playfield registers
	sta WSYNC
	sta GRP0
	lda PFBitmap0,x
	sta PF0		; store first playfield byte
	lda PFBitmap1,x
	sta PF1		; store 2nd byte
	lda PFBitmap2,x
	sta PF2		; store 3rd byte
; Here's the asymmetric part -- by this time the TIA clock
; is far enough that we can rewrite the same PF registers
; and display new data on the right side of the screen
	txa
	ror
	tay ; pause to let playfield finish drawing
	lda PFBitmap3,x
	sta PF0		; store 4th byte
	lda PFBitmap4,x
	sta PF1		; store 5th byte
	lda PFBitmap5,x
	sta PF2		; store 6th byte
	dex
	bne ScanLoop	; repeat until all scanlines drawn
; Reset playfield
	SLEEP 14	; give time to finish drawing scanline
	lda #0
	sta PF0
	sta PF1
	sta PF2		; clear playfield

	TIMER_SETUP 28
	TIMER_WAIT
	jmp NextFrame


; SetHorizPos routine
; A = X coordinate
; X = player number (0 or 1)
SetHorizPos
	sta WSYNC	; start a new line
	sec		; set carry flag
DivideLoop
	sbc #15		; subtract 15
	bcs DivideLoop	; branch until negative
	eor #7		; calculate fine offset
	asl
	asl
	asl
	asl
	sta RESP0,x	; fix coarse position
	sta HMP0,x	; set fine offset
	rts		; return to caller

